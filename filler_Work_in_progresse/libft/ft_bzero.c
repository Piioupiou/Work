#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*str;

	str = s;
	if (n >= 1)
	{
		int	i;

		i = 0;
		while (n > 0)
		{
			str[i] = 0;
			i++;
			n--;
		}
	}
}
