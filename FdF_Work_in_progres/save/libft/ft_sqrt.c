int	ft_sqrt(int nb)
{
	int	i;
	int	tmp;

	i = 0;
	tmp = 0;
	if (nb > 2147395600)
		return (0);
	while (i <= (nb / 2))
	{
		tmp = i * i;
		if (tmp == nb && (nb % i) == 0)
			return(i);
		 i++;
	}
	return (0);
}
